package es;
public class main11 {
    public static void main(String[] args) {
        // Definícia polomeru
        double radius = 7.5;

        // Výpočet obvodu
        double circumference = 2 * Math.PI * radius;
        System.out.println("Obvod je = " + circumference);

        // Výpočet plochy
        double area = Math.PI * radius * radius;
        System.out.println("Oblasť je = " + area);
    }
}

